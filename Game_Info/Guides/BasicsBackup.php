<p id="Stats"></p>
Now keep in mind this is like not any other server around the game, we have multiple stats for each level, and you can distribute them however you like
<ul>
	<li>STR (Strength)</li>
		<ul>
			<li>Increases all physical damage</li>
		</ul>
	<li>DEX (Dexterity)</li>
		<ul>
			<li>Increases Evasion</li>
			<li>Increases Aim</li>
		</ul>
	<li>END (Endurance)</li>
		<ul>
			<li>Increases HP</li>
			<li>Increases Physical defense</li>
		</ul>
	<li>INT (Intelligence)</li>
		<ul>
			<li>Increases magical damage</li>
		</ul>
	<li>SPR (Spirit)</li>
		<ul>
			<li>Increases magic defense</li>
			<li>Increaes Spirit Points (SP)</li>
			<li>Increases Critical Hit Chance</li>
		</ul>
</ul>

