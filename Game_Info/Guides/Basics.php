<h3 id="Stats">Stats</h3>
<p>Each time you level you will obtain skill points which you may distribute however you like. </p>
<img src="../Assets/img/GameInfo/StatePic.png"></img>
<ul>
<li>STR (Strength)</li>
	<ul>
		<li>Increases all physical damage</li>
	</ul>
<li>DEX (Dexterity)</li>
	<ul>
		<li>Increases Evasion</li>
		<li>Increases Aim</li>
	</ul>
<li>END (Endurance)</li>
	<ul>
		<li>Increases HP</li>
		<li>Increases Physical defense</li>
		<li>Increases Shield Block Rate</li>
	</ul>
<li>INT (Intelligence)</li>
	<ul>
		<li>Increases magical damage</li>
	</ul>
<li>SPR (Spirit)</li>
	<ul>
		<li>Increases magic defense</li>
		<li>Increaes Spirit Points (SP)</li>
		<li>Increases Critical Hit Chance</li>
	</ul>
</ul>

<div class="alert alert-danger" role="alert">
<p>Remember not to go over +150 stat points on one stat or else it will bug, but if you forget, you can always reset your stats with a stat reset capsule from Pey.</p>
</div>

