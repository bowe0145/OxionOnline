<h4 id="Fighter">Fighter</h4>

<p>
A fighter is a melee class that excells at dealing damage as well as taking it. A fighter is versatile being able to wield an axe, a 2 handed sword, or a 1 handed sword and shield. Fighters are great party levelers since they have the ability to group opponents for other people to kill them. 
</p>

	<ul>
		<li>Knight</li>
	</ul>
<img src="../Assets/img/GameInfo/Knight101.png"></img>
<p>
The Knight uses a 1-handed sword and shield or a 2-handed sword. Knights are the main tanks but can pack quite a punch as well. They can keep aggro of a lot of monsters to keep them in place for others to kill them. As a Knight you'll want to go an END based build or STR, depending on wether you want to deal damage or tank more. If you prefer to PvP it's recommended to implement some DEX in your build.
</p>
	<ul>
		<li>Gladiator</li>
	</ul>
<img src="../Assets/img/GameInfo/Gladiator101.png"></img>
<p>	
The Gladiator uses an axe or a 2-handed sword. They focus on dealing heavy damage to a single target. Gladiators aren't a tank, but are able to take a hit. You won't have much use in a leveling party, but the damage you can deal to bosses will make up for that. As a Gladiator you'll want to build STR and DEX base as this will make you land harder and more frequent hits while also giving you the evasion needed to survive.
</p>

	
<h4 id="Cleric">Cleric</h4>

<p>
A cleric is a support class, helping their allies with heals, health and spirit boosts, as well as damage and defense boosts. As a Cleric, you will be focused on keeping your friends alive throughout your adventure rather than dealing damage. You will be using your divine power in raids later in your adventure and you will be a key factor in the game. You will always have a shield and as a weapon you will either equip a mace if you are a HolyKnight or a hammer if you are a Guardian.
</p>

	<ul>
		<li>HolyKnight</li>
	</ul>
	<img src="../Assets/img/GameInfo/HolyKnight101.png"></img>
<p>
HolyKnights equip a mace and shield and are known to have "super buffs". As a Holyknight not only will you heal, but you can also use crit buff on the highest damage dealer to ensure maximum damage output. 
</p>

	<ul>
		<li>Guardian</li>
	</ul>
<img src="../Assets/img/GameInfo/Guardian101.png"></img>
<p>
Guardian equip a hammer and shield and have the best heals. Guardians are the only class in the game with the ability to instantly revive themselves, they do this through the use of Rebirth. Rebirth can cast on him/herself or an allied target. 
</p>

<h4 id="Mage">Mage</h4>

<p>
A Mage is a ranged class that excells at dealing magic damage. While they deal a lot of damage, Mages have very low defense and will die very quickly. Mages have high AOE damage and work well with Fighters and Clerics. As a Mage you will use a staff if you are a Wizard or a wand if you are a Warlock.  
</p>

	<ul>
		<li>Wizard</li>
	</ul>
<img src="../Assets/img/GameInfo/Wizard101.png"></img>
<p>
Wizards equip a staff and have maximum AOE damage. Wizards are highly suited for leveling parties and PVE. 
</p>

	<ul>
		<li>Warlock</li>
	</ul>
<img src="../Assets/img/GameInfo/Warlock101.png"></img>
<p>
Warlocks equip a wand and while they have the capabilty to AOE, their AOEs are not as strong as Wizard. Warlocks deal high single target damage, making them the more suitable pick for PVP. 
</p>

<h4 id="Archer">Archer</h4>

<p>
An Archer is a ranged class that deals high physical damage to enemies, using powerful poisons to bring high sustained damage. Archers are great at solo leveling since they have the ability to kite their enemies. As an Archer you will use a crossbow if you are a Ranger or a bow if you are a SharpShooter. You will have low defense but high evasion helps keep you alive.
</p>

	<ul>
		<li>SharpShooter</li>
	</ul>
<img src="../Assets/img/GameInfo/SharpShooter101.png"></img>
<p>
SharpShooters equip a bow and deal high physical single target damage making them highly valuable in damage dealing parties as well as PVP. Sharpshooters have the ability to stun and disarm their opponents. 
</p>

	<ul>
		<li>Ranger</li>
	</ul>
<img src="../Assets/img/GameInfo/Ranger101.png"></img>
<p>
Rangers equip a crossbow and have powerful AOEs. Rangers can hide from their enemies and are best suited for PVE. 
</p>

<h4 id="Trickster">Trickster</h4>

<p>
A trickster is a melee class that uses claws or dual swords to slay their foes. 
The Trickster is one of the most versatile classes in the game, from dealing an extremely high amount of damage with few skills. 
You are the most slippery class of all and are able to escape sticky situations. 
This will come greatly to your advantage while fighting your way through Isya. 
Tricksters are great for solo and party leveling bringing very high teamplay potential. 
You will equip dualswords if you are a Spectre or claws if you are a Reaper. 
</p>

	<ul>
		<li>Reaper</li>
	</ul>
<img src="../Assets/img/GameInfo/Reaper101.png"></img>
<p>
Reapers use claws to take down their foes. Equipped with a variety of different damage dealing skills, they can quickly take down even the toughest Knight.
</p>

	<ul>
		<li>Spectre</li>
	</ul>
<img src="../Assets/img/GameInfo/Spectre101.png"></img>
<p>
Spectres use dual swords to deal more damage, but they lose a bit of speed. They are most commonly used for PVE, but are very capable at killing players as well.
</p>

